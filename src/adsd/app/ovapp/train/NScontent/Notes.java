package adsd.app.ovapp.train.NScontent;

public class Notes{
    private String value;
    private String key;
    private String type;
    private String noteType;
    private String priority;
    private String routeIdxFrom;
    private String routeIdxTo;
    private String isPresentationRequired;
    private String category;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNoteType() {
        return noteType;
    }

    public void setNoteType(String noteType) {
        this.noteType = noteType;
    }

    public String getPriority() {
        return priority;
    }

    public void setPriority(String priority) {
        this.priority = priority;
    }

    public String getRouteIdxFrom() {
        return routeIdxFrom;
    }

    public void setRouteIdxFrom(String routeIdxFrom) {
        this.routeIdxFrom = routeIdxFrom;
    }

    public String getRouteIdxTo() {
        return routeIdxTo;
    }

    public void setRouteIdxTo(String routeIdxTo) {
        this.routeIdxTo = routeIdxTo;
    }

    public String getIsPresentationRequired() {
        return isPresentationRequired;
    }

    public void setIsPresentationRequired(String isPresentationRequired) {
        this.isPresentationRequired = isPresentationRequired;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }
}